#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	unsigned int	mem_size;
	char			*pt_dest;

	mem_size = ft_strlen(src) + 1;
	pt_dest = malloc(mem_size);
	ft_strcpy(pt_dest, src);
	return (pt_dest);
}
